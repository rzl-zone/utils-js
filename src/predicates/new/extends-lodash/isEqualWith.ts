import type { IsEqualCustomizer } from "@/types/private/predicates/new/isEqualWith";
import { baseDeepEqual } from "./private/baseDeepEqual";

/** ----------------------------------------------------
 * * ***This method is like `isEqual` except that it accepts `customizer` which
 * is invoked to compare values. If `customizer` returns `undefined`, comparisons
 * are handled by the method instead. The `customizer` is invoked with up to
 * six arguments: (objValue, othValue [, index|key, object, other, stack]).***
 * ----------------------------------------------------
 *
 * @param {*} value The value to compare.
 * @param {*} other The other value to compare.
 * @param {Function} [customizer] The function to customize comparisons.
 * @returns {boolean} Returns `true` if the values are equivalent, else `false`.
 * @example
 *
 * function isGreeting(value) {
 *   return /^h(?:i|ello)$/.test(value);
 * }
 *
 * function customizer(objValue, othValue) {
 *   if (isGreeting(objValue) && isGreeting(othValue)) {
 *     return true;
 *   }
 * }
 *
 * var array = ['hello', 'goodbye'];
 * var other = ['hi', 'goodbye'];
 *
 * isEqualWith(array, other, customizer);
 * // => true
 */
export function isEqualWith(
  value: unknown,
  other: unknown,
  customizer?: IsEqualCustomizer
): boolean {
  return baseDeepEqual(value, other, customizer, new WeakMap());
}
